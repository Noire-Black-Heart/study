package application;

public class PoolImage {
	private String path;
	private double offx, offy, sizex, sizey;
	
	public double getOffx() {
		return this.offx;
	}
	
	public void setOffx(double x) {
		this.offx = x;
	}
	
	public double getOffy() {
		return this.offy;
	}
	
	public void setOffy(double y) {
		this.offy = y;
	}
	
	public double getSizex() {
		return this.sizex;
	}
	
	public void setSizex(double x) {
		this.sizex = x;
	}
	
	public double getSizey() {
		return this.sizey;
	}
	
	public void setSizey(double y) {
		this.sizey = y;
	}
	
	public String getPath() {
		return this.path;
	}
	
	public void setPath(String path) {
		this.path = path;
	}
	
}
